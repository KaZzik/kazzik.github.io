     <p align="center"><b>Отмена действий</b></p>
     <p>&nbsp;</p>
     <p>Не ошибается только тот, кто ничего не делает. Это выражение применимо и к работе с системой AutoCAD. Нередко при черчении случается допустить ошибку, которую потом приходится исправлять.</p>
     <p>Отменить неверно выполненное действие в AutoCAD просто. Для этого следует воспользоваться кнопкой <img src="images\image006 (2).jpg">&nbsp;&nbsp; — <b>Отменить (Undo)</b> в панели быстрого доступа программы. Однократное нажатие этой кнопки вызы¬вает отмену последнего выполненного действия. Если ошибка была допущена не на последнем действии, а раньше, отобразить список выполненных ранее команд поможет кнопка<img src="images\image012.jpg">&nbsp; , расположенная правее кнопки<img src="images\image006 (2).jpg">&nbsp; .</p>
     <p>Открыв список последних выполненных действий, щелкните мышью на том, которое предшествовало ошибочному. Чтобы вернуть последнее отмененное действите, нажмите кнопку<img src="images\image011 (3).jpg">&nbsp;&nbsp; — <b>Вернуть (Redo)</b>. Отобразить список отмененных действий поможет кнопка<img src="images\image012.jpg">&nbsp;&nbsp; справа от кнопки<img src="images\image011 (3).jpg">&nbsp; .</p>
     <p>Отменить действие или вернуть его можно только до очередного сохранения документа, так как список всех выполненных или отмененных действий находятся в оперативной памяти компьютера.</p>
     <p>После сохранения все изменения, внесенные в документ, записываются на жестком диске, и список всех выполненных или отмененных действий становится недоступным.</p>
