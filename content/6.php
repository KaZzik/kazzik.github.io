        <p align="center"><b>Удаление объектов</b></p>
     <p>&nbsp;</p>
     <p>При построении всегда возникают ситуации, когда с чертежа требуется убрать лишние элементы. Это может быть неправильно построенный объект, вспомогательные прямые линии или лучи, точки и т.д.</p>
     <p>Для удаления объектов в AutoCAD предусмотрена команда<b> Стереть (_Erase)</b>.</p>
     <p>1- Введите с клавиатуры команду <b>Стереть (Erase) </b>и нажмите клавишу Enter. В командной строке появится запрос: <b>Выберите объекты:</b> (Select objects:).</p>
     <p>Выделяя объекты, вы указываете, к каким элементам чертежа нужно применить команду.</p>
     <p>2- Щелкните мышью на объектах, которые требуется удалить. Выбранные объекты обозначатся мелкой штриховкой.</p>
     <p>&nbsp;</p>
     <p><b>Можно удалить объекты и другим способом.</b></p>
     <p>1. Щелкните мышью на объектах, которые требуется удалить.</p>
     <p>2. В группе элементов управления <b>Редактирование (Edit) ленты Главная (Ноmе)</b> нажмите правую верхнюю кнопку —<b> Стереть (Erase)</b><img src="images\image004 (2).jpg"> -&nbsp; . Выбранные объекты исчезнут с чертежа.</p>
     <p>&nbsp;</p><br>
     <p align="center"><img src="images\image001 (3).png"></p>
     <p align="center">Группа элементов управления <b>Редактирование</b></p>
     <p>&nbsp;</p>
     <p>Следующий способ, который можно применить к удаляемому объекту.</p>
     <p>1. В группе элементов управления <b>Редактирование (Edit) </b>ленты <b>Главная (Ноmе)</b> нажмите кнопку<img src="images\image004 (2).jpg">&nbsp;&nbsp; —<b> Стереть (Erase)</b>.</p>
     <p>2. Щелкните мышью на удаляемом объекте, чтобы выделить его.</p>
     <p>3. Нажмите клавишу <b>Enter</b>. Объект удален.</p>
     <p>&nbsp;</p>
     <p>И еще один способ, который можно применить к удаляемому объекту.</p>
     <p>1. Щелкните мышью на удаляемом объекте, чтобы выделить его.</p>
     <p>2. Нажмите клавишу <b>Delete</b>. Объект удален. Удалять можно как один объект, так и несколько одновременно. Для этого нужно выделить все удаляемые объекты, после чего подтвердить удаление с помощью клавиши <b>Enter </b>или нажав кнопку<img src="images\image004 (2).jpg">&nbsp; .</p>
