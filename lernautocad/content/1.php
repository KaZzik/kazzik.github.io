      <p align="center">Понятие графических примитивов</p>
      <p>&nbsp;</p>
      <p>&nbsp;&nbsp; Чертежи в AutoCAD состоят из набора графических примитивов.</p>
      <p><span>&nbsp;&nbsp; Графический примитив </span>— элемент чертежа, обрабатываемый редактором как одно целое. Команды построения графических примитивов содержатся в панели инструментов <span>Рисование</span> и в меню <span>Рисование</span>. Кроме этого, выполнение нужной команды можно осуществить через командную строку.</p>
      <p>Примитивы могут быть простыми и сложными.</p>
      <p><span>&nbsp;&nbsp; Простые </span>примитивы состоят из отдельных элементов. К простым примитивам относятся следующие объекты: Точка, Отрезок, Прямая, Луч, Круг (окружность), Эллипс, Дуга, Сплайн, Текст.</p>
      <p><span>&nbsp;&nbsp; Сложные </span>примитивы – это те примитивы, которые можно путем специальной операции расчленения разложить на более&nbsp; простые. К сложным примитивам относятся: полилиния, мультилиния, мультитекст, размер, выноска, таблица, допуск, штриховка, вхождение блока или внешней ссылки, атрибут и растровое изображение.</p>
      <p>&nbsp;&nbsp;&nbsp; К <span>пространственным</span>&nbsp; графическим примитивам относятся геометрические тела.<br><br></p>
      <p align="center"><img src="images/image002.jpg"></p>
           <p><br><br>&nbsp;&nbsp; Все графические построения в AutoCAD ведутся по одному и тому же принципу с помощью графических примитивов. Создаются все эти разные чертежи по одним и тем же общим правилам.</p>
      <p>&nbsp;&nbsp; Линия в AutoCAD является базовым примитивом. Линии бывают различного рода — одиночные отрезки, ломаные (с сопряжениями дугами или без них), пучки параллельных линий (мультилинии), а также эскизные. Линии рисуют, задавая координаты точек, свойства (тип, цвет и др.), значения углов. Отрезки могут быть одиночными или объединенными в ломаную линию. Несмотря на то, что сегменты соприкасаются в конечных точках, каждый из них представляет собой отдельный объект.</p>
